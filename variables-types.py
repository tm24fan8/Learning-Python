# Numbers

myint = 7
print(myint)

myfloat1 = 7.0
print(myfloat1)

myfloat2 = float(7)
print(myfloat2)

# Strings
# The difference between the two is that using double quotes makes it easy to
# include apostrophes (whereas these would terminate the string if using single
# quotes)

mystring1 = 'hello'
print(mystring1)

mystring2 = "hello"
print(mystring2)

mystring3 = "Don't worry about apostrophes"
print(mystring3)

# Operators

one = 1
two = 2
three = one + two
print(three)

hello = "hello"
world = "world"
helloworld = hello + " " + world
print(helloworld)

# Multiple variables assigned on same line
a, b = 3, 4
print(a, b)

